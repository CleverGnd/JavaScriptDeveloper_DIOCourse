// Aula: 02 - Estruturas Condicionais - If/Else

const numero = 5;
// % é o operador de resto da divisão e === é o operador de igualdade
const ehNumeroPar = (numero % 2) === 0;

// if e else são estruturas condicionais
if (ehNumeroPar) {
    console.log('O número é par');
} else {
    console.log('O número é impar');
}
